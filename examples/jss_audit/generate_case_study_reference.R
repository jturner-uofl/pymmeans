                       bias.adj = TRUE, sigma = sigma_pigs))
wcsv(data.frame(source = sba$source, response = sba$response, SE = sba$SE,
                sigma = sigma_pigs), p("pigs_emm_response_biasadj.csv"))
# log-scale pairwise -> ratio (back-transform of a difference)
spp <- emm_sum(summary(contrast(emmeans(pigs.lm, ~ source), "pairwise"),
                       type = "response", adjust = "none"))
wcsv(data.frame(contrast = spp$contrast, ratio = spp$ratio, SE = spp$SE),
          p("pigs_pairs_ratio.csv"))

