# Two independent Poisson responses sharing the same design — `brms` would
# fit this jointly via Stan; the Python equivalent is a PyMC joint
# Poisson model + `posterior_emm_summary`. The R reference is the
# per-response Poisson GLM (which equals the brms/joint-likelihood result
# when the response components are conditionally independent).
# ---------------------------------------------------------------------------
set.seed(55); n_mp <- 240
g_mp <- factor(sample(c("gA","gB","gC"), n_mp, replace = TRUE))
