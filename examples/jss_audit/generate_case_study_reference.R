  wcsv(data.frame(g=s$g, emmean=s$emmean, SE=s$SE),
       p(sprintf("nuisance_%s_emm.csv", cs$label)))
}
cat("SECTION VIII done\n")

# ===========================================================================
# SECTION IX — Cox proportional hazards (survival::coxph)
# ===========================================================================
# The Cox partial likelihood does not identify the baseline hazard, so the
# link-scale EMM is a RELATIVE log-hazard; the identifiable, scale-invariant
# quantity is the log-hazard-RATIO contrast (pairs). We cross-validate those
# against R survival::coxph + emmeans. (ordinal / multinomial / survey
# references are the committed test-suite cross-validations in
# ../../tests/r_reference, copied into cs_ref/.)
if (requireNamespace("survival", quietly = TRUE)) {
  library(survival)
  set.seed(909)
  ncx <- 200
  dcx <- data.frame(g = factor(sample(c("a", "b", "c"), ncx, replace = TRUE)),
                    x = rnorm(ncx))
  lp <- (dcx$g == "b") * 0.5 + (dcx$g == "c") * 1.0 + 0.3 * dcx$x
  dcx$time <- rexp(ncx, rate = exp(lp))
  dcx$status <- rbinom(ncx, 1, 0.8)
  wcsv(dcx, p("cox_data.csv"))
  cfit <- coxph(Surv(time, status) ~ g + x, data = dcx)
  cct <- as.data.frame(summary(contrast(emmeans(cfit, ~ g), "pairwise"),
                               adjust = "none"))
  wcsv(data.frame(contrast = cct$contrast, estimate = cct$estimate,
                       SE = cct$SE, p = cct$p.value),
            p("cox_pairs.csv"))
  # IX.b: STRATIFIED Cox. `strata(site)` lets each site carry its own
  # baseline hazard h_{0,s}(t); the linear-predictor coefficients (and
  # hence the log-hazard-ratio EMM contrasts) come from the stratified
  # partial likelihood. pymmeans handles this transparently because
  # statsmodels PHReg(strata=) exposes the same params / vcov shape.
  set.seed(909 + 1); n_s <- 400
  d_s <- data.frame(
    g    = factor(sample(c("gA","gB","gC"), n_s, replace = TRUE)),
    x    = rnorm(n_s),
    site = factor(sample(c("s1","s2","s3"), n_s, replace = TRUE))
  )
  lp_s <- (d_s$g == "gB") * 0.5 + (d_s$g == "gC") * 1.0 + 0.3 * d_s$x
  d_s$T      <- rexp(n_s, rate = exp(lp_s))
  d_s$status <- rbinom(n_s, 1, 0.8)
  wcsv(d_s, p("cox_strata_data.csv"))
  fit_s <- coxph(Surv(T, status) ~ g + x + strata(site), data = d_s)
  cct_s <- as.data.frame(summary(contrast(emmeans(fit_s, ~ g), "pairwise"),
                                 adjust = "none"))
  wcsv(data.frame(contrast = cct_s$contrast, estimate = cct_s$estimate,
                  SE = cct_s$SE, p = cct_s$p.value),
       p("cox_strata_pairs.csv"))
  # IX.c: Weibull AFT (parametric survival, R survreg). The linear
  # predictor lives on the log-time scale: log T = X β + σ ε. R's
  # survreg(dist="weibull") and lifelines WeibullAFTFitter use the same
  # AFT parameterisation so β agrees to optimiser precision, and the
  # pymmeans EMM machinery (driven by from_aft → ModelInfo) reproduces
  # emmeans(survreg, ...) at the same ~1e-6 solver tolerance.
  set.seed(42); n_a <- 300
  g_a <- factor(sample(c("gA","gB","gC"), n_a, replace = TRUE))
  x_a <- rnorm(n_a)
  lp_a <- (g_a == "gB") * 0.5 + (g_a == "gC") * 1.0 + 0.3 * x_a
  T_a <- exp(lp_a + 0.5 * rnorm(n_a))
  E_a <- rbinom(n_a, 1, 0.85)
  d_a <- data.frame(g = g_a, x = x_a, T = T_a, E = E_a)
  wcsv(d_a, p("aft_data.csv"))
  fit_a <- survreg(Surv(T, E) ~ g + x, data = d_a, dist = "weibull")
  em_a <- emmeans(fit_a, ~ g)
  sa <- as.data.frame(summary(em_a))
  wcsv(data.frame(g = sa$g, emmean = sa$emmean, SE = sa$SE, df = sa$df),
       p("aft_emm.csv"))
  ct_a <- as.data.frame(summary(contrast(em_a, "pairwise", adjust = "none")))
  wcsv(data.frame(contrast = ct_a$contrast, estimate = ct_a$estimate,
                  SE = ct_a$SE, df = ct_a$df, p_value = ct_a$p.value),
       p("aft_pairs.csv"))
  # Additional AFT distributions (lognormal, loglogistic) on the same
  # seeded fixture. lifelines uses different params_ block names per
  # distribution (mu_ / alpha_ / lambda_); the pymmeans `from_aft`
  # adapter detects the location block by matching patsy design columns.
  for (dist in c("lognormal", "loglogistic")) {
    fit_d <- survreg(Surv(T, E) ~ g + x, data = d_a, dist = dist)
    ct_d <- as.data.frame(summary(contrast(emmeans(fit_d, ~ g),
                                           "pairwise", adjust = "none")))
    wcsv(data.frame(contrast = ct_d$contrast, estimate = ct_d$estimate,
                    SE = ct_d$SE, df = ct_d$df, p_value = ct_d$p.value),
         p(sprintf("aft_pairs_%s.csv", dist)))
  }
  # IX.d: Cox PH with Gaussian frailty (random hazard effect on a
  # clustering id). R `coxph(... + frailty(id, "gaussian"))` uses
  # Laplace approximation; pymmeans bridges this via a PyMC Cox-
  # partial-likelihood + Gaussian-frailty model + `posterior_emmeans`
  # — validated in the notebook (§XII.3) at convergence accuracy.
  set.seed(99); n_cl_f <- 12; n_per_f <- 18; n_f <- n_cl_f * n_per_f
  id_f <- factor(rep(seq_len(n_cl_f), each = n_per_f))
  g_f  <- factor(sample(c("gA","gB","gC"), n_f, replace = TRUE))
  x_f  <- rnorm(n_f)
  u_f  <- rnorm(n_cl_f, sd = 0.4)
  lp_f <- (g_f == "gB") * 0.5 + (g_f == "gC") * 1.0 + 0.3 * x_f +
          u_f[as.integer(id_f)]
  d_f  <- data.frame(
    id = id_f, g = g_f, x = x_f,
    status = rbinom(n_f, 1, 0.8),
    T = rexp(n_f, rate = exp(lp_f))
  )
  wcsv(d_f, p("cox_frailty_data.csv"))
  fit_f <- coxph(Surv(T, status) ~ g + x + frailty(id, distribution = "gaussian"),
                 data = d_f)
  ct_f <- as.data.frame(summary(contrast(emmeans(fit_f, ~ g),
                                         "pairwise", adjust = "none")))
  wcsv(data.frame(contrast = ct_f$contrast, estimate = ct_f$estimate,
                  SE = ct_f$SE),
       p("cox_frailty_pairs.csv"))
  # Capture R's MLE: β coefs + Laplace frailty variance (no SE for the
  # frailty variance in R's standard output — Bayesian posterior gives
  # a credible interval instead).
  bf <- coef(fit_f)
  wcsv(data.frame(
    quantity = c(names(bf), "sigma_sq_frailty"),
    value    = c(unname(bf), fit_f$history$frailty$theta)
  ), p("cox_frailty_R.csv"))
  # IX.e: Cox PH with GAMMA frailty (the other R-supported distribution).
  # Parametrisation: u ~ Gamma(1/theta, 1/theta) so E[u]=1, Var[u]=theta.
  fit_fg <- coxph(Surv(T, status) ~ g + x +
                  frailty(id, distribution = "gamma"), data = d_f)
  ct_fg <- as.data.frame(summary(contrast(emmeans(fit_fg, ~ g),
                                          "pairwise", adjust = "none")))
  wcsv(data.frame(contrast = ct_fg$contrast, estimate = ct_fg$estimate,
                  SE = ct_fg$SE),
       p("cox_gamma_frailty_pairs.csv"))
  # IX.f: Generalized-Gamma AFT — survreg doesn't expose this dist, so R's
  # reference is flexsurvreg (which R emmeans does not directly support).
  # Capture the β coefs only; the pymmeans contract is "lifelines β
  # matches R flexsurv β to ~1e-6 (the Weibull / LogN / LogLog optimiser
  # tolerance), and from_aft + emmeans then build the EMM via the same
  # Lβ algebra validated for the other AFT distributions in §XI".
  if (requireNamespace("flexsurv", quietly = TRUE)) {
    library(flexsurv)
    fit_gg <- flexsurvreg(Surv(T, E) ~ g + x, data = d_a, dist = "gengamma")
    cf <- coef(fit_gg)
    # The covariate block (mu + ggB + ggC + x) — exclude scale/shape (sigma, Q).
    cv_names <- c("mu", "ggB", "ggC", "x")
    wcsv(data.frame(coef = cv_names, value = unname(cf[cv_names])),
         p("aft_gengamma_R.csv"))
  }
  cat("SECTION IX (Cox + strata + AFT + Gaussian/Gamma frailty + gengamma) done\n")
} else {
  cat("SECTION IX (Cox) skipped — survival not available\n")
}

