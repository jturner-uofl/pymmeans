# ===========================================================================
# SECTION I — Core algebra: grid alignment, L-matrix, EMM across model families
# ===========================================================================
set.seed(101)
n <- 300
d1 <- data.frame(
  A = factor(sample(c("a", "b", "c"), n, replace = TRUE)),
  B = factor(sample(c("p", "q"), n, replace = TRUE)),
  x = rnorm(n)
)
eta1 <- 0.4 + (d1$A == "b") * 0.7 + (d1$A == "c") * 1.3 +
  (d1$B == "q") * 0.5 + 0.6 * d1$x
d1$y      <- eta1 + rnorm(n, sd = 0.8)                # gaussian
d1$ybin   <- rbinom(n, 1, plogis(eta1 - 0.4))         # logistic
d1$ycount <- rpois(n, exp(0.2 + 0.3 * (d1$A == "c") + 0.4 * d1$x))  # poisson
d1$ypos   <- rgamma(n, shape = 2, rate = exp(-(0.5 + 0.3 * (d1$B == "q"))))
wcsv(d1, p("core_data.csv"))

# OLS: EMM of A + L matrix + reference grid
fit_ols <- lm(y ~ A + B + x, data = d1)
emA <- emmeans(fit_ols, ~ A)
sA <- emm_sum(emA)
wcsv(data.frame(A = sA$A, emmean = sA$emmean, SE = sA$SE, df = sA$df,
                     lower = sA$lower.CL, upper = sA$upper.CL),
          p("core_ols_emm_A.csv"))
L <- emA@linfct; colnames(L) <- names(coef(fit_ols))
wcsv(as.data.frame(L), p("core_ols_Lmatrix.csv"))
wcsv(as.data.frame(ref_grid(fit_ols)@grid), p("core_ols_refgrid.csv"))

# OLS by-group: A | B + pairs
emAB <- emmeans(fit_ols, ~ A | B)
sAB <- emm_sum(emAB)
wcsv(data.frame(A = sAB$A, B = sAB$B, emmean = sAB$emmean, SE = sAB$SE),
          p("core_ols_emm_A_by_B.csv"))

# joint_tests(): the omnibus Type-III-style F-test per model term (df1, df2,
# F-ratio, p). The factor-level F-test a reviewer expects alongside the EMMs.
jt <- as.data.frame(joint_tests(fit_ols))
wcsv(data.frame(term = jt[["model term"]], df1 = jt$df1, df2 = jt$df2,
                F_ratio = jt$F.ratio, p_value = jt$p.value),
     p("joint_tests_ols.csv"))

# Logistic GLM: link + response
fit_logit <- glm(ybin ~ A + B + x, data = d1, family = binomial())
sL <- emm_sum(emmeans(fit_logit, ~ A))
wcsv(data.frame(A = sL$A, emmean = sL$emmean, SE = sL$SE,
                     lower = sL$asymp.LCL, upper = sL$asymp.UCL),
          p("core_logit_emm_link.csv"))
sLr <- emm_sum(emmeans(fit_logit, ~ A, type = "response"))
wcsv(data.frame(A = sLr$A, prob = sLr$prob, SE = sLr$SE,
                     lower = sLr$asymp.LCL, upper = sLr$asymp.UCL),
          p("core_logit_emm_response.csv"))

# Poisson GLM: response (rate) + rate-ratio pairs
fit_pois <- glm(ycount ~ A + x, data = d1, family = poisson())
sPr <- emm_sum(emmeans(fit_pois, ~ A, type = "response"))
wcsv(data.frame(A = sPr$A, rate = sPr$rate, SE = sPr$SE),
          p("core_poisson_emm_response.csv"))
sPp <- emm_sum(summary(contrast(emmeans(fit_pois, ~ A), method = "pairwise"),
                       type = "response", adjust = "none"))
wcsv(data.frame(contrast = sPp$contrast, ratio = sPp$ratio, SE = sPp$SE,
                     p = sPp$p.value),
          p("core_poisson_pairs_ratio.csv"))

# Gamma GLM (log link): response
fit_gam <- glm(ypos ~ B + x, data = d1, family = Gamma(link = "log"))
sG <- emm_sum(emmeans(fit_gam, ~ B, type = "response"))
wcsv(data.frame(B = sG$B, response = sG$response, SE = sG$SE),
          p("core_gamma_emm_response.csv"))
