# ===========================================================================
# SECTION V — Multiplicity adjustments
# ===========================================================================
set.seed(505)
g <- factor(rep(c("ctrl", "t1", "t2", "t3", "t4"), each = 24))
ym <- as.integer(g) * 0.18 + rnorm(length(g))
dm <- data.frame(g = g, y = ym)
wcsv(dm, p("multiplicity_data.csv"))
fitm <- lm(y ~ g, data = dm)
emm_m <- emmeans(fitm, ~ g)
# Pairwise family under several adjustments.
for (adj in c("none", "tukey", "sidak", "holm", "bonferroni", "BH", "scheffe")) {
  sa <- emm_sum(summary(contrast(emm_m, "pairwise"), adjust = adj))
  wcsv(data.frame(contrast = sa$contrast, estimate = sa$estimate,
                       SE = sa$SE, t = sa$t.ratio, p = sa$p.value),
            p(sprintf("mult_pairwise_%s.csv", adj)))
}
# General (rank>1) multivariate-t adjustment over the FULL pairwise family —
# 10 correlated contrasts among 5 means. This is the hard QMC case, distinct
# from the rank-1 Dunnett shared-control special case below; emmeans drives it
# through mvtnorm's randomised QMC, so we seed for a reproducible reference.
set.seed(5150)
smvt <- emm_sum(summary(contrast(emm_m, "pairwise"), adjust = "mvt"))
wcsv(data.frame(contrast = smvt$contrast, estimate = smvt$estimate,
                SE = smvt$SE, t = smvt$t.ratio, p_mvt = smvt$p.value),
     p("mult_pairwise_mvt.csv"))
# Dunnett many-to-one MVT at several k.
for (k in c(3, 5, 8)) {
  levs <- c("ctrl", paste0("t", seq_len(k)))
  set.seed(600 + k)
  gg <- factor(rep(levs, each = 25), levels = levs)
  yy <- as.integer(gg) * 0.15 + rnorm(length(gg))
  dk <- data.frame(g = gg, y = yy)
  wcsv(dk, p(sprintf("dunnett_data_k%d.csv", k)))
  fk <- lm(y ~ g, data = dk)
  sk <- emm_sum(summary(contrast(emmeans(fk, ~ g), "trt.vs.ctrl"),
                        adjust = "mvt"))
  wcsv(data.frame(contrast = sk$contrast, estimate = sk$estimate,
                       SE = sk$SE, t = sk$t.ratio, df = sk$df, p_mvt = sk$p.value),
            p(sprintf("dunnett_k%d.csv", k)))
}

# --- Inference utilities on the same 5-group design: equivalence /
# non-inferiority (test(delta=, side=)) and compact letter display (cld). ---
# Equivalence (TOST), delta = 0.5, two-sided: H0 |theta| >= 0.5 (not equiv)
# vs H1 within +/- 0.5. R triggers TOST automatically when delta > 0.
eqv <- as.data.frame(test(emm_m, delta = 0.5))
wcsv(data.frame(g = eqv$g, p_value = eqv$p.value), p("equiv_emm.csv"))
# Non-inferiority, delta = 0.3, right-sided, on the unadjusted pairwise family.
nin <- as.data.frame(test(contrast(emm_m, "pairwise", adjust = "none"),
                          delta = 0.3, side = ">"))
wcsv(data.frame(contrast = nin$contrast, p_value = nin$p.value),
     p("equiv_noninf.csv"))
# Compact letter display (multcomp::cld), Tukey-adjusted, alpha = 0.05. Only
# emitted if multcomp is installed; otherwise the notebook validates cld() as a
# self-consistency check against the (separately R-validated) Tukey p-values.
if (requireNamespace("multcomp", quietly = TRUE)) {
