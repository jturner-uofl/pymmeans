# ===========================================================================
# SECTION X — Multivariate-response OLS + mvcontrast (the "mlm" path)
# ===========================================================================
# R `emmeans` on a multivariate `lm` (`cbind(y1,y2,y3) ~ ...`) introduces a
# `rep.meas` pseudo-factor (one level per response column) and offers
# `mvcontrast` for Hotelling-T² / F-tests across responses. pymmeans's
# matching surface is `multivariate_emmeans` + `mvcontrast`.
set.seed(1010)
n_mv <- 60
g_mv <- factor(sample(c("a", "b", "c"), n_mv, replace = TRUE))
x_mv <- rnorm(n_mv)
beta_mv <- c(a = 0, b = 0.5, c = 1)
y1 <- beta_mv[as.character(g_mv)] + 0.3 * x_mv + rnorm(n_mv, sd = 0.5)
y2 <- 0.7 * beta_mv[as.character(g_mv)]      + rnorm(n_mv, sd = 0.5)
y3 <- 0.4 * beta_mv[as.character(g_mv)] + 0.2 * x_mv + rnorm(n_mv, sd = 0.5)
mv_data <- data.frame(g = g_mv, x = x_mv, y1 = y1, y2 = y2, y3 = y3)
wcsv(mv_data, p("mv_data.csv"))
mv_fit <- lm(cbind(y1, y2, y3) ~ g + x, data = mv_data)
em_mv <- as.data.frame(emmeans(mv_fit, ~ g | rep.meas))
wcsv(data.frame(
  g       = em_mv$g,
  rep_meas = em_mv$rep.meas,
  emmean  = em_mv$emmean, SE = em_mv$SE, df = em_mv$df,
  lower_cl = em_mv$lower.CL, upper_cl = em_mv$upper.CL
), p("mv_emm.csv"))
mvc <- as.data.frame(mvcontrast(
  emmeans(mv_fit, ~ g, by = "rep.meas"), "pairwise", mult.name = "rep.meas"
))
wcsv(data.frame(
  contrast = mvc$contrast, T_square = mvc$T.square,
  df1 = mvc$df1, df2 = mvc$df2, F_ratio = mvc$F.ratio, p_value = mvc$p.value
), p("mv_mvc.csv"))
cat("SECTION X (multivariate / mvcontrast) done\n")

