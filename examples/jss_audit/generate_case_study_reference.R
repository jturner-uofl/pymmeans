emm_sum <- function(em) as.data.frame(summary(em))

# wcsv() — write a data.frame to CSV with FULL float64 precision. R's
# write.csv() serialises doubles at ~15 significant digits, which does NOT
# round-trip an IEEE-754 double (17 are required). The Python side would then
# parse SLIGHTLY different doubles than R fit on, injecting a spurious ~1e-15
# disagreement into otherwise-deterministic comparisons. formatC(., 17, "g")
# emits the exact shortest-round-tripping representation, so pandas reconstructs
# the identical bit pattern. (The residual ~1e-14 that remains is purely the
# difference between R's and NumPy's linear-algebra routines — irreducible and
# expected, NOT a data-serialisation artefact.)
wcsv <- function(df, file) {
  df[] <- lapply(df, function(col)
    if (is.numeric(col)) trimws(formatC(col, digits = 17, format = "g")) else col)
  write.csv(df, file, row.names = FALSE)
}
