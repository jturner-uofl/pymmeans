  cldf$.group <- gsub(" ", "", as.character(cldf$.group))
  wcsv(data.frame(g = cldf$g, group = cldf$.group), p("cld.csv"))
}
cat("SECTION V done\n")

# ===========================================================================
# SECTION VI — Mixed-model df (Satterthwaite / Kenward-Roger / vc_formula)
# ===========================================================================
if (ok_mixed) {
  set.seed(606)
  ns <- 24; nps <- 8
  subj <- factor(rep(seq_len(ns), each = nps))
  trt <- factor(rep_len(c("A", "B"), ns * nps))
  re <- rnorm(ns, sd = 1.2)[as.integer(subj)]
  yv <- 2 + (trt == "B") * 0.8 + re + rnorm(ns * nps, sd = 0.9)
  dmix <- data.frame(subj = subj, trt = trt, y = yv)
  wcsv(dmix, p("mixed_ri_data.csv"))
  m_ri <- lmerTest::lmer(y ~ trt + (1 | subj), data = dmix)
  satt <- emm_sum(emmeans(m_ri, ~ trt, lmer.df = "satterthwaite"))
  wcsv(data.frame(trt = satt$trt, emmean = satt$emmean, SE = satt$SE,
                       df = satt$df), p("mixed_ri_satt.csv"))
  kr <- emm_sum(emmeans(m_ri, ~ trt, lmer.df = "kenward-roger"))
  wcsv(data.frame(trt = kr$trt, emmean = kr$emmean, SE = kr$SE,
                       df = kr$df), p("mixed_ri_kr.csv"))

  # vc_formula via the proven oats fixture (nested Block/Variety variance
  # component). statsmodels MixedLM converges to the same non-boundary
  # solution as lme4 here; an arbitrary synthetic crossed design can collapse
  # one component to the boundary in statsmodels (a known optimiser
  # difference), so we cross-validate on the canonical oats data instead.
  # Matches the Sprint-7.A fixture: a 63-row subset, Block random intercept
  # + Block:Variety variance component.
  oats <- read.csv(p("oats_data.csv"))
  names(oats)[names(oats) == "yield"] <- "yld"
  drop_idx <- c(1, 2, 3, 5, 8, 13, 21, 34, 55)   # R 1-based; Sprint-7.A subset
  oats_sub <- oats[-drop_idx, ]
  oats_sub$BlockVariety <- factor(paste(oats_sub$Block, oats_sub$Variety,
                                        sep = ":"))
  oats_sub$Block <- factor(oats_sub$Block)
  oats_sub$Variety <- factor(oats_sub$Variety)
  oats_sub$nitro <- factor(oats_sub$nitro)
  wcsv(oats_sub, p("oats_vc_data.csv"))
  m_vc <- lmerTest::lmer(
    yld ~ Variety + nitro + (1 | Block) + (1 | BlockVariety), data = oats_sub)
  vkr <- emm_sum(emmeans(m_vc, ~ nitro, lmer.df = "kenward-roger"))
  wcsv(data.frame(nitro = vkr$nitro, emmean = vkr$emmean, SE = vkr$SE,
                       df = vkr$df), p("oats_vc_kr.csv"))
  # lme4's REML variance-component estimates — used by the notebook to
  # demonstrate that the ~4% KR-df gap is a FIT-level difference
  # (statsmodels' REML optimiser lands on slightly different variances)
  # rather than a method-level KR-df bug.
  vcdf <- as.data.frame(VarCorr(m_vc))
  wcsv(data.frame(component = vcdf$grp, variance = vcdf$vcov),
            p("oats_vc_varcomp.csv"))
  # vc_formula NESTED-MODEL F-test reference: KRmodcomp + SATmodcomp on
  # the sub-plot `nitro` fixed effect (4 levels => df_num = 3), with the
  # Block + Block:Variety variance components present. Cross-validates
  # pymmeans's krmodcomp()/satmodcomp() on a variance-component model.
  big_vc <- lmerTest::lmer(
    yld ~ Variety + nitro + (1 | Block) + (1 | BlockVariety), data = oats_sub)
  small_vc <- lmerTest::lmer(
    yld ~ Variety + (1 | Block) + (1 | BlockVariety), data = oats_sub)
  krf <- KRmodcomp(big_vc, small_vc)$test
  satf <- SATmodcomp(big_vc, small_vc)$test
  wcsv(data.frame(
    method   = c("KR", "SAT"),
    stat     = c(as.numeric(krf["Ftest", "stat"]), as.numeric(satf[1, "statistic"])),
    ndf      = c(as.numeric(krf["Ftest", "ndf"]),  as.numeric(satf[1, "ndf"])),
    ddf      = c(as.numeric(krf["Ftest", "ddf"]),  as.numeric(satf[1, "ddf"])),
    p_value  = c(as.numeric(krf["Ftest", "p.value"]), as.numeric(satf[1, "p.value"]))
  ), p("oats_vc_ftest.csv"))
  cat("SECTION VI done\n")
} else {
  cat("SECTION VI skipped (lme4/lmerTest/pbkrtest not available)\n")
}

