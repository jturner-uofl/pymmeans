# ===========================================================================
# SECTION VIII — Weighting sensitivity (unbalanced design)
# ===========================================================================
# On an UNBALANCED design, averaging the nuisance factor with equal vs
# proportional (cell-frequency) weights gives DIFFERENT — both correct —
# EMMs. We cross-validate pymmeans against R under both schemes to show
# the weighting choice is honoured exactly.
set.seed(808)
# B is distributed unevenly WITHIN each A level, so equal != proportional.
mk <- function(a, np_, pq) data.frame(
  A = a, B = sample(c("p", "q"), np_, replace = TRUE, prob = c(pq, 1 - pq)))
sb <- rbind(mk("a", 90, 0.8), mk("b", 50, 0.4), mk("c", 40, 0.5))
sb$A <- factor(sb$A); sb$B <- factor(sb$B)
sb$y <- (sb$A == "b") * 0.5 + (sb$A == "c") * 1.0 + (sb$B == "q") * 0.7 +
  rnorm(nrow(sb), sd = 0.8)
wcsv(sb, p("weights_data.csv"))
fit_w <- lm(y ~ A + B, data = sb)
for (wt in c("equal", "proportional", "cells")) {
  sw <- emm_sum(emmeans(fit_w, ~ A, weights = wt))
  wcsv(data.frame(A = sw$A, emmean = sw$emmean, SE = sw$SE),
            p(sprintf("weights_emm_%s.csv", wt)))
}
# --- VIII.b: nuisance × weights interaction. A 3-factor unbalanced design
# (g target × b nuisance × c remaining non-target) lets `nuisance='b'` flip
# *only* the b-dimension to equal-marginal averaging while c retains the
# requested weighting scheme. R's `wt.nuis='equal'` (the default in
# `ref_grid(..., nuisance=)`) is the semantic pymmeans now matches under
# weights='outer' and weights='proportional'.
set.seed(2024)
n_nw <- 240
g_nw <- factor(sample(c("g1","g2","g3"), n_nw, replace = TRUE, prob = c(.5,.3,.2)))
b_nw <- factor(sample(c("bL","bH"),       n_nw, replace = TRUE, prob = c(.8,.2)))
c_nw <- factor(sample(c("cA","cB","cC"),  n_nw, replace = TRUE, prob = c(.6,.3,.1)))
y_nw <- (g_nw=="g2")*0.4 + (g_nw=="g3")*1.0 + (b_nw=="bH")*0.7 +
        (c_nw=="cB")*0.5 + (c_nw=="cC")*1.0 + rnorm(n_nw, sd=0.7)
nw_data <- data.frame(g=g_nw, b=b_nw, c=c_nw, y=y_nw)
wcsv(nw_data, p("nuisance_data.csv"))
m_nw <- lm(y ~ g + b + c, data=nw_data)
nw_cases <- list(
  list(label="prop_no_nuis",  call=function() emmeans(m_nw, ~ g, weights="proportional")),
  list(label="prop_nuis_b",   call=function() emmeans(m_nw, ~ g, weights="proportional", nuisance="b", wt.nuis="equal")),
  list(label="prop_nuis_bc",  call=function() emmeans(m_nw, ~ g, weights="proportional", nuisance=c("b","c"), wt.nuis="equal")),
  list(label="outer_nuis_b",  call=function() emmeans(m_nw, ~ g, weights="outer",         nuisance="b", wt.nuis="equal"))
)
for (cs in nw_cases) {
  s <- as.data.frame(summary(cs$call()))
  wcsv(data.frame(g=s$g, emmean=s$emmean, SE=s$SE),
       p(sprintf("nuisance_%s_emm.csv", cs$label)))
}
# Cells × nuisance reference: the analytical-extrapolation path that
# pymmeans now matches. R `wt.nuis='equal'` handles empty
