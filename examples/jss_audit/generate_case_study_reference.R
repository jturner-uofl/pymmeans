lp1 <- 1.0 + (g_mp=="gB")*0.4 + (g_mp=="gC")*0.9 + 0.2*x_mp
lp2 <- 0.5 + (g_mp=="gB")*0.3 + (g_mp=="gC")*1.1 + 0.4*x_mp
d_mp <- data.frame(
