    alpha_gm = pm.HalfNormal("alpha_gm", 10.0)
    u_gm = pm.Gamma("u_gm", alpha=alpha_gm, beta=alpha_gm, shape=n_cl_cf)
    eta_gm = pt.dot(Xs, beta_gm) + pt.log(u_gm[cs])
